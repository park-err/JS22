$(".clear").mouseover(function() {
    $("#clear1").css("background-color", "darkgray");
    $("#clear2").css("background-color", "darkgray");
    $("#clear3").css("background-color", "darkgray");
    $("#clear4").css("background-color", "darkgray");
})

$(".clear").mouseleave(function() {
    $("#clear1").css("background-color", "gray");
    $("#clear2").css("background-color", "gray");
    $("#clear3").css("background-color", "gray");
    $("#clear4").css("background-color", "gray");
})